
//! Not commonly used so you can skip it

//! Bitwise AND (&)
const andResult = 5 & 1; // 0101 & 0001 = 0001
console.log("Bitwise AND:", andResult); // Output: 1

//! Bitwise OR (|)
const orResult = 5 | 1; // 0101 | 0001 = 0101
console.log("Bitwise OR:", orResult); // Output: 5

//! Bitwise XOR (^)
const xorResult = 5 ^ 1; // 0101 ^ 0001 = 0100
console.log("Bitwise XOR:", xorResult); // Output: 4

//! Bitwise NOT (~)
const notResult = ~5; // ~0101 = 1010
console.log("Bitwise NOT:", notResult); // Output: -6

//! Left Shift (<<)
const leftShiftResult = 5 << 1; // 0101 << 1 = 1010
console.log("Left Shift:", leftShiftResult); // Output: 10

//! Signed Right Shift (>>)
const rightShiftResult = 5 >> 1; // 0101 >> 1 = 0010
console.log("Signed Right Shift:", rightShiftResult); // Output: 2

//! Unsigned Right Shift (>>>)
const unsignedRightShiftResult = 5 >>> 1; // 0101 >>> 1 = 0010
console.log("Unsigned Right Shift:", unsignedRightShiftResult); // Output: 2
