//! EQUAL TO

console.log(5 == 8); // false
console.log(5 == "5"); // true

//! STRICT EQUAL TO

console.log(5 === 5); // true
console.log(5 === "5"); // false

//! NOT EQUAL TO

console.log(5 != 8); // true
// ? 5 is not equal to 8 it means it is false . Not operator reverses it by making true to false and false to true so the answer will be true

//! STRICT NOT EQUAL TO
//? equal so false
console.log(5 !== 5); // false
//? false so equal
console.log(5 !== "5"); // true

//! GREATER THAN
console.log(10 > 8); // true

//! LESS THAN

console.log(10 < 8); // false

//! GREATER THAN OR EQUAL TO
console.log(10 >= 8); // true

//! LESS THAN OR EQUAL TO

console.log(10 <= 8); // false
